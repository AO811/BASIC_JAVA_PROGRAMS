class POSITIVE_NEGATIVE
{
    public static void main(String args[])
    {
        int a=100;
        if(a>0)
        {
            System.out.println("The number "+a+" is positive.");
        }
        else if(a<0)
        {
            System.out.println("The number "+a+" is negative.");
        }
        else
        {
            System.out.println("The number "+a+" is 0.");
        }
    }
}